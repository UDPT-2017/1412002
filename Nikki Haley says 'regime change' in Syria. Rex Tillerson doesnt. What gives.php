    <h3>Nikki Haley says 'regime change' in Syria. Rex Tillerson doesn't. What gives?
</h3>

<p>
Depending on which Sunday talk show you watched, you came away with a very different sense of what the Trump Administration's next steps will be in Syria.</p>
<p>
If you watched UN Ambassador Nikki Haley on CNN, you heard her tell "State of the Union" host Jake Tapper that "regime change is something that we think is going to happen because all of the parties are going to see that (Syrian president Bashar al-)Assad is not the leader that needs to be taking place for Syria."</p><p>
<div style="width: 100%;
text-align:center;"">
<div class="image"><img src="../photo/ap17097567004485.jpg" ></div>
<p style="alignCenter">Rex Tillerson
</p>
</div></p>
<p>
If you caught Secretary of State Rex Tillerson on CBS's "Face the Nation" you heard something very different. Asked by "Face" host John Dickerson whether removing Assad was a priority for the United States, Tillerson offered this: "Our priority in Syria, John, really hasn't changed. I think the President has -- been quite clear. First and foremost, we must defeat ISIS."</p>
<p>Uh, what? Haley seemed to be quite clearly announcing that regime change was a priority in Syria. Tillerson pointedly said it was not and that nothing at all had changed in terms of America's policy and priorities in Syria.</p>
<p>Why the confusion? My guess is that it's not confusion at all. It's two factions within the foreign policy wing of the Trump White House trying to convince the president of the rightness of their positions on Syria via public channels.</p>
<p>At first blush that might seem odd. Wouldn't these sorts of debates be better hashed out in private so as to show a united front to the political world and the American people once a decision is reached?</p>
<p>In a traditional White House with a president who plays by the political rules, sure. But this not a traditional White House. And Trump loves breaking the rules much more than he likes adhering to them.</p>
<p>What we learned in the campaign is that Trump aides -- counselor Kellyanne Conway was the master -- would purposely go on TV if they wanted to make sure Trump got a particular message. They knew -- and know -- how much cable TV he watches and quickly grasped that the best way to reach him oftentimes was to go on TV.</p>
<p>This passage from the New Yorker's excellent Conway profile is telling in regards the importance Trump places on TV:</p>
<p>She said that she was trying to spend more time on campaign management, but for Trump a measure of her success was her presence on television. "I've cut my TV time in half," she told me. "And he's, like, 'I didn't see you on TV in the last hour. Where are you?' I'm, like, 'Mr. Trump, managing the campaign means talking to the state directors and the mail house and the R.N.C.' "</p>
<p>Trump himself has even acknowledged how much information he gains from watching TV. Asked who he consulted with on military matters during the campaign, Trump said this to NBC's Chuck Todd: "Well, I watch the shows. I mean, I really see a lot of great -- you know, when you watch your show, and all of the other shows, and you have the generals, and you have certain people."</p>
<p>The simple fact is that Trump doesn't have a long-held belief about what we should do in Syria. That's evidenced by the fact that he long insisted the United States needed to stay out of Syria's business but then reversed course entirely in the wake of a chemical attack against civilians in the northwestern part of the country last week.</p>
<p>Trump touted that reversal as a sign of his "flexibility," his willingness to let events and other peoples' views change his own. That's an open invitation for his advisers to try to shape what's next in Syria because it's not entirely clear that Trump favors one view over another.
His habit is to let his advisers duke it out in public -- sort of like an "Apprentice"-boardroom scene -- and then come in at the last minute and make the call. It's how he thinks the best decisions get made.</p>
<p>That, it seems to me, is what Haley and Tillerson are up to at the moment -- hashing out their divergent views in full view of the president in hopes of convincing him of the rightness of their argument.</p>
<p>Strange in any other administration. In this one? Standard operating procedure.</p>








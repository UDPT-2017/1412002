    <h3>AMD to cut wires from VR headsets with Nitero acquisition

</h3>
 


<p>In AMD's world, a virtual reality headset that wires you to a PC isn't fun.</p>


<p>So the chip company has acquired Nitero, which provides technology for VR headsets to be wirelessly connected to PCs or mobile devices.</p>


<p>Nitero's technology will help device makers build VR headsets that wirelessly connect to devices in the multi-gigabit throughput range, according to AMD.</p>

<div style="width: 100%;
text-align:center;"">
<div class="image"><img src="../photo/20160315-2_edited-100717409-large.jpg" ></div>

</div>
<p>With the technology, users will be able to take advantage of powerful graphics processors on PCs. At the same time, users will be able to move freely.
</p>

<p>Wireless headsets are widely considered to be the future of VR. Wired headsets like the Oculus Rift and the HTC Vive provide great graphics but limit movement.</p>


<p>Some wireless all-in-one VR headsets like Microsoft's HoloLens are available, but they have limited battery life and graphics capabilities. A headset called Sulon Q with AMD's FX-8800P and eight integrated Radeon R7 graphics cores was shown at last year's CES but hasn't shipped yet.</p>


<p>Nitero's intellectual property will be used to allow other vendors to build wireless headsets, Drew Prairie, an AMD spokesman, said in an email. </p>


<p>AMD won't build its own head-mounted displays but will serve as a one-stop shop for other vendors developing virtual reality products because it has the CPU, GPU, and now Nitero's high-bandwidth wireless technology, Prairie said.</p>


<p>Nitero in the past has introduced products based on WiGig, a high-speed wireless communications technology that can transfer data at multi-gigabit speeds. Intel's wire-free PC plans are also based on WiGig, which can wirelessly connect external displays and external hard drives to PCs.</p>


<p>Nitero holds many patents that are based on establishing stable wireless connections using WiGig technology. To implement Nitero technology, chipsets would be required in both the device running VR applications and the wirelessly connected headsets.</p>


<p>By tying VR headsets to PCs wirelessly, AMD could sell more PC chips and graphics cards. However, it's not yet known how the company will implement Nitero's technology.</p>


<p>AMD declined to comment on how much it paid for Nitero. But the acquisition plays into AMD's aggressive pursuit of VR.</p>









<p>It wouldn’t be the first time that a Middle Eastern conflict has shaken China into action over North Korea.</p>
<p>In February 2003, U.S. President George W. Bush told his then Chinese counterpart, Jiang Zemin: “If we could not solve the problem diplomatically, I would have to consider a military strike against North Korea.” Just a month later the U.S. invaded Iraq, and Jiang clearly feared a similar intervention in China’s backyard. By August, China was chairing the six-party nuclear-disarmament talks — involving North and South Korea, the U.S., Russia, Japan and China — and kept them going for six years through sheer diplomatic will despite numerous occasions when principle stakeholders stormed out. It was an unprecedented about-face for China, which had previously shunned any global leadership or mediation role.</p>
<p>John Park, director of the Korea Working Group at the Harvard Kennedy School, says Trump’s current language and behavior "feels like a walk down memory lane ... I’m wondering if this is an attempt to repeat Bush’s playbook.”</p>
<p>Of course, today’s Korean Peninsula is radically different from 2003, not least because Pyongyang’s nuclear program is significantly more advanced. Supreme Leader Kim Jong Il, the father of Kim Jong Un, had enough conventional weaponry trained on Seoul — just 30 miles from the DMZ — to devastate the South Korean capital. But Kim Jong Un has sufficient nuclear material for more than a dozen atomic bombs. There is no weathering that storm. And while the elder Kim was willing to publicly embrace at least the rhetoric of nuclear disarmament — even as he enhanced his country's nuclear capabilities in secret — his son insists nukes are “nonnegotiable.” A Pyongyang spokesman said Thursday's strike on Syria "proves a million times over" that North Korea is correct to strengthen its nuclear program.</p>
<p>There are also real doubts over what leverage Beijing still has over the Kim regime. Modern China’s founder Mao Zedong famously described the neighbors as “as close as lips and teeth,” and his eldest son died fighting in the Korean War. Today, though, mutual animosity is intense. For North Korea, China’s market reforms make it a traitor to the goal of a global socialist utopia; Beijing sees Pyongyang as petulant and unpredictable. Even though Beijing made its position clear to Pyongyang by supporting unprecedented U.N. sanctions following a fourth North Korean nuclear test last February, there has since been a fifth test, as well as a slew of missile launches.</p>
<p>After Beijing banned coal imports from North Korea — depriving the regime of a cash earner worth $1 billion in 2015 — a furious North Korea accused China of “dancing to the tune of the U.S.” and began timing missile launches at provocative moments for Beijing. There were launches during China's most important political convention (the annual meeting of the National Committee of the Chinese People's Political Consultative Conference) in March, and immediately before the last week’s Xi-Trump summit.</p>
<p>Additionally, “North Korea has insulted in personal terms, using extremely vulgar language, the President of China,” says Scott Harold, an East Asia expert at RAND Corp. “Everyone knows how Xi Jinping was called out by Kim Jong Un.”</p>
<p>China is in a bind. It does have significant sway over North Korea’s economy — being responsible for 90% of Pyongyang's trade — but if it squeezes too hard, it risks imposing deprivation, and possibly even famine, on millions of ordinary North Koreans. If resulting instability caused the collapse of the regime, waves of refugees would pour into China. Even worse, a U.S.-allied South Korea would lay claim to the entire peninsula, presenting the possibility of U.S. troops and armaments being deployed right on China's doorstep.</p>
<p>Beijing could do more in terms of enforcement, like ramping up domestic law-enforcement efforts to target the syndicates of corrupt Chinese officials and North Korean businessmen who supply materials for nuclear and missile development. “That’s where a lot of the procurement is happening,” says Park.But that would only slow rather than solve the problem. “The bottom line is that nobody has a solution to what is going on in North Korea,” says Carlyle Thayer, emeritus professor at the Australian Defence Force Academy. “What do you force China to do? Once again, Trump has put himself out beyond the ability to deliver.”</p>










    <h3>Unilateral Action by Trump on North Korea Will Simply Add to an Already Intractable Problem</h3>
       
<p>
When U.S. President Donald Trump dispatched 59 Tomahawk missiles against Syria on Thursday evening — ostensibly in response to a chemical-weapons attack attributed to Syrian President Bashar Assad, which activists say claimed 86 lives — his guest maintained remarkable composure.</p>

<p>Dining with Trump at the Mar-a-Lago resort at the time of the strike was Chinese President Xi Jinping. Trump has repeatedly told Xi that the Chinese leader needs to do more to rein in rogue state North Korea’s nuclear program. In a Financial Times interview published April 2, Trump claimed that “China has great influence over North Korea.” He added he was prepared to act unilaterally against the regime of Supreme Leader Kim Jong Un if Beijing refused to cooperate. "If they do [help] that will be very good for China,” said Trump, "and if they don’t it won’t be good for anyone."</p>
<p>On Sunday, with the Syria strikes still ringing in Xi’s ears, Trump accented his threats by dispatching the U.S. Navy Carl Vinson Strike Group — an aircraft carrier and other warships — to the Korean Peninsula. "The No. 1 threat in the region continues to be North Korea, due to its reckless, irresponsible and destabilizing program of missile tests and pursuit of a nuclear weapons capability," said U.S. Pacific Command spokesman Dave Benham. The message was received loud and clear: on Sunday, China’s state-backed Global Times newspaper carried an editorial entitled, “After Syria Strikes, Will North Korea Be Next?”</p>
<div style="width: 100%;
text-align:center;"">
<div class="image"><img src="../photo/gettyimages-665777762.jpg" ></div>
<p style="alignCenter">U.S. President Donald Trump listens during an expanded bilateral meeting with Chinese President Xi Jinping, not in the picture, at the Mar-a-Lago estate in West Palm Beach, Fla., on April 7, 2017

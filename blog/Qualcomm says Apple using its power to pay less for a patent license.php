    <h3>Qualcomm says Apple using its power to pay less for a patent license

</h3>
       


<p>Qualcomm is seeking damages from Apple, alleging that the iPhone maker interfered with long-term agreements between the chip company and licensees that manufacture the iPhone and iPad, and encouraged actions by regulators against the company by giving government agencies "false and misleading information."</p>

<p>Apple did not use certain high-performance features of the Qualcomm chipsets for the iPhone 7, claims Qualcomm. When iPhones with the Qualcomm chipset still outperformed iPhones that used a chipset from key rival Intel, Apple is said to have publicly claimed that there was “no discernible difference” between iPhones using Intel chipsets and those using Qualcomm chipsets, when it knew the opposite to be true, according to a filing Monday by Qualcomm.</p>

<div style="width: 100%;
text-align:center;"">
<div class="image"><img src="../photo/0786_as-17-100703647-large.jpg" ></div>
<p style="alignCenter">Qualcomm says Apple using its power to pay less for a patent license</p>
</div>
<p>Apple warned that if Qualcomm were to make or sponsor public comparisons of the Intel and Qualcomm-based iPhones, Apple would use the marketing resources at its disposal to “retaliate” against Qualcomm and its standing as an Apple chipset supplier would be jeopardized.</p>

<p>The filing Monday by Qualcomm in the U.S. District Court for the Southern District of California is in response to a lawsuit Apple filed against the company in January, accusing the chip supplier of charging "exorbitant" licensing fees for its cellular technology.</p>

<p>Apple is seeking nearly US$1 billion in compensation for paying excessive royalties to Qualcomm. The company said in its filing in January that Qualcomm's licensing model is based on the final selling price of a device, so that the royalty paid for its standard-essential patents by makers of high-value phones will be higher than that paid by makers of basic cell phones.
</p>
<p>The smallest salable unit for a cellular SEP license should be no greater than the baseband processor chipset, Apple argued. It also wants Qualcomm to negotiate single-patent licenses rather than offer a license to its patents as a single portfolio.</p>

<p>Both companies have a lot at stake in the dispute. Apple would like to find ways to lower the cost of its iPhones, particularly in newer, price-sensitive markets, while Qualcomm makes a significant portion of its revenue from licensing its intellectual property.</p>

<p>In its response Monday, Qualcomm portrays Apple as using its considerable power to try to get lower royalty rates from the chip company. After filing the lawsuit in the court in California, Apple also sued Qualcomm in China, the U.K. and Japan as part of “its aggressive strategy of constructing commercial disputes and then claiming it has been victimized,” according to the filing.</p>

<p>On Monday, when asked for comment, a spokesman for Apple referred back to the company's statement in January in which it claimed that Qualcomm had insisted on charging royalties for technologies they have nothing to do with.</p>

<p>"The more Apple innovates with unique features such as TouchID, advanced displays, and cameras, to name just a few, the more money Qualcomm collects for no reason and the more expensive it becomes for Apple to fund these innovations," the iPhone maker said in that statement, when filing the suit against Qualcomm in the California court.</p>

<p>Apple said that Qualcomm has taken “increasingly radical steps,” including most recently by withholding nearly $1 billion in payments from Apple as retaliation for its "truthful responses" to law enforcement agencies investigating the chip company.</p>

<p>Qualcomm holds that “late-comer” Apple contributed virtually nothing to the development of core cellular technology and has depended heavily on Qualcomm technology to become the dominant player in the cellphone market. “Now, Apple wants to pay far less than fair value for a license to Qualcomm’ s patents,” it added.</p>

<p>The chip maker alleged that Apple interfered, and continues to interfere, with the chip maker’s long- standing contracts with the manufacturers of Apple’s cellular devices, leading them to withhold certain royalties owed to Qualcomm, according to the redacted filing.</p>

<p>The contracts with manufacturers like Compal, Wistron, Pegatron and Foxconn were signed even before Apple started using Qualcomm chipsets in its products. Rather than enter into a contract directly with Qualcomm, Apple chose to rely on the contracts Qualcomm had with the manufacturers.</p>

<p>The chip maker also claims that Apple has withheld money owed to Qualcomm under another contract relating to a high-speed feature of its chipset.</p>

<p>In negotiations between the two companies for a patent agreement, Apple is said to have offered Apple to pay Qualcomm royalties per phone that would be “a small fraction of the royalty” paid by other smartphone vendors.</p>

<p>Qualcomm also accuses Apple of giving false information to regulators. The chip company was fined in December by South Korea’s antitrust regulator for violating antitrust laws. By January, the U.S. Federal Trade Commission accused it in a lawsuit of strong-arming phone makers into accepting unfair licensing terms while giving Apple a break in exchange for exclusivity.</p>

<p>The chip company now charges that Apple misled the Korea Fair Trade Commission on August 17, 2016 , by testifying, for example,  that “Apple has yet to add a [second chipset] supplier because of Qualcomm’s exclusionary conduct”. Apple had already at the time added Intel as a second baseband chip supplier and purchased Intel chips to include in the iPhone 7, which was only a few weeks away from its September release, according to the filing.</p>








